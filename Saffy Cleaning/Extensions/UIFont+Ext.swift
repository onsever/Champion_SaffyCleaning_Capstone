import UIKit

extension UIFont {
    
    static func urbanistBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Urbanist-Bold", size: size)
    }
    
    static func urbanistRegular(size: CGFloat) -> UIFont? {
        return UIFont(name: "Urbanist-Regular", size: size)
    }
    
    static func urbanistMedium(size: CGFloat) -> UIFont? {
        return UIFont(name: "Urbanist-Medium", size: size)
    }
    
    static func urbanistSemibold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Urbanist-SemiBold", size: size)
    }
    
}
